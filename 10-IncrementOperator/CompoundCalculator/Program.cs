using System;

int score = 100;

Console.WriteLine($"초기 점수 : {score}");
score += 25;
Console.WriteLine($"25점 추가 후 : {score}");
score -= 15;
Console.WriteLine($"15점 감소 후 : {score}");
score *= 25;
Console.WriteLine($"2배 증가 후 : {score}");
score /= 4;
Console.WriteLine($"4로 나눈 후 : {score}");
score %= 7;
Console.WriteLine($"7로 나눈 나머지 : {score}");
Console.WriteLine();
Console.WriteLine();
